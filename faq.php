               <p>Проверьте сообщения в Таксометре. Возможно, вы были заблокированы. Соответствующее уведомление ищите в разделе «Сообщения» Таксометра.</p>

               <p>Смена района. Возможно, в районе, где вы находитесь сейчас мало заказов.</p>

