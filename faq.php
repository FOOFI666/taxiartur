               <h3> 5. Что такое Фотоконтроль?</h3>
           </div>
           
           <div class="otvet">
               В программу «Яндекс.Про» включен Фотоконтроль

               <p>Каждые 10 дней система будет предлагать сделать фото автомобиля, салона, БСО.</p>

               <p>За несколько дней до следующего фотоконтроля в приложении будут предупреждения о необходимости прохождения фотоконтроля. Если у Вас будет возможность пройти фотоконтроль заранее – не пренебрегайте этой возможностью – т.к. процесс проверки фотоконтроля может занимать определенное время.</p>

               <p>Рекомендуем, при фотоконтроле не делать иные фотографии которые не подходят по норме фотоконтроля т.к. в этом случае Вас Яндекс ЗАБЛОКИРУЕТ, и РАЗБЛОКИРОВКА может длиться от 1 часа до суток.</p>

               <p>Фотографии автомобиля должны быть четкими, без срезанных углов автомобиля, крыши автомобиля и т. д.</p>

               <p>Для того чтобы сфотографировать салон автомобиля, сначала откройте его переднюю дверь, отойдите в сторону на пол метра, сделайте фото передних сидений, затем откройте заднюю дверь, отойдите в сторону, сделайте фото задних сидений. Во время темного времени суток при фотографировании салона необходимо включить в салоне верхний свет и активировать вспышку в меню фотоконтроля в Таксометре.</p>

               <p>Если запрос фотоконтроля поступил в темное время суток, его необходимо сделать в хорошо освещенном месте. Например, на заправке, мойке или у торгового центра.</p>


