               <h3>4. Что делать если не приходят заказы?</h3>
           </div>
           
           <div class="otvet">
               <b>Необходимо принимать более 60% заказов.</b> Помните, что важно принимать более 60% заказов, в противном случае доступ к сервису будет временно приостановлен.

               <p>Не отменяйте много заказов. Если вы уже приняли заказ, отменяйте его только в крайнем случае — это сказывается на распределении заказов в будущем. Ваша #активность понизится, что приведет к меньшему поступлению заказов.</p>

               <p>Статус «Занят» Проверьте — возможно, Вы включили статус «Занят». Возможно, Вы часто меняете Статусы: «На линии» — «Занят», «На линии – «Занят» и т. д. Дополнительно в Таксометре предусмотрена защита от случайного снижения показателей. Она действует так: если водитель не принял три заказа подряд, включается статус «Занят». Таксометр считает, что вы отвлеклись или заняты. Если ситуация под контролем, просто включите снова «На линии».</p>

               <p>Проверьте интернет на устройстве. Принимая заказы на телефон, помните, что во время разговора интернет не работает и сигнал прервется. Если сигнал неожиданно пропал, включите режим «В самолете», подождите несколько секунд и выключите его, проверьте, есть ли интернет и включена ли Геолокация.</p>

               <p>Проверьте GPS (модуль, который принимает сигналы с навигационных спутников). Если передача GPS-сигнала отключена, заказы приходить не будут. Проверьте, подключен ли, на Вашем устройстве GPS?</p>

               <p>GPS может не работать по следующим причинам:</p>

               <p>– GPS вообще отсутствует в Вашем телефоне.</p>

               <p>– GPS отключен в настройках Вашего телефона, проверьте настройки.</p>

               <p>– Вы находитесь в экранируемой зоне, в помещении ( в данной ситуации положите телефон например на подоконник) или если Вы находитесь на улице (необходимо поменять месторасположение). Во всех вышеперечисленных случаях местонахождение водителя система видеть не будет, и следовательно, заказы поступать не будут.</p>

               <p>Рейтинг. Если у водителя низкий рейтинг (ниже «4») – это означает блокировку на определенное время – получение заказов не возможно.</p>

